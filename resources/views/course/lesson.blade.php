<h3 class="headtxt">Урок 1: Для чего нужно беспокоиться о безопасности паролей?</h3>

<h4>Почему мы не заботимся о безопасности своих паролей и какие ошибки допускаем?</h5>
<p class="lesson">Каждый день мы множество раз пользуемся паролями, даже если не замечаем этого. Пароли защищают наши учетные записи на рабочих
    и личных компьютерах, наши социальные сети, электронную почту, самые важные папки и документы.<br><br>
    Чтобы завести новый аккаунт на любом интернет-ресурсе, нам приходится придумывать еще один пароль в дополнение
    к имеющимся и все их каким-то образом хранить или запоминать.
</p>
<div class="block">
    <img src="{{ asset('storage/lessons_img/1/pass.png') }}" alt="">
    <blockquote>&laquoВероятно, Вы слышали о том, что пароль должен быть длиной не менее восьми символов
     и в нем должны присутствовать не только буквы, но и цифры.&raquo</blockquote>
</div>
<p class="lesson">
    Мы можем не заботиться о безопасности своих паролей, поскольку нам кажется, что мы не представляем интереса
    для мошенников. В результате <em>мы храним пароли как попало:</em>  28% пользователей записывают пароли в блокнот,<br>
    а 9% — на какой-нибудь листок рядом с компьютером.<br><br>
    Мы думаем, что преступников интересуют более масштабные цели — например, банковские серверы или аккаунты
    голливудских звезд, — но это далеко не всегда является правдой.<br><br>
    По этой же причине мы часто ленимся придумывать сложные пароли и используем простые и очень простые
    комбинации символов. Подобрать такие пароли очень легко.
    Однако, даже если мы хотим придумать сложный пароль, то не всегда знаем, каким он должен быть,
    чтобы легко запоминаться и надежно защищать наши данные.<br><br>
    Часто мы не понимаем, почему уже запомненный надежный пароль — например, от корпоративной почты
    — нельзя использовать в других местах, ведь его будет одинаково трудно взломать.<br><br>
    <em>К слову, о взломе:</em>  мы обычно не беспокоимся, когда кто-то пытается взломать наши учетные записи.
    Нам кажется, что неудачная попытка взлома не стоит внимания, если мошенники не достигли цели.
    Наконец, мы можем по неосторожности доверить свои пароли родственникам, коллегам, знакомым,
    даже если не знаем, известны ли им хотя бы основы информационной безопасности.
</p>
